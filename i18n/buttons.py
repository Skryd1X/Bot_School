def has_btn(key: str) -> bool:
    return key in _BUTTONS

def btn(lang: str | None, key: str) -> str:
    return pick_lang(lang, _BUTTONS.get(key, {DEFAULT_LANG: key}))

def all_btn_variants(key: str):
    m = _BUTTONS.get(key) or {}
    return tuple(sorted(set((v or "").strip() for v in m.values() if v)))

def is_btn(text: str, key: str) -> bool:
    t = (text or "").strip()
    return t in set(all_btn_variants(key))
